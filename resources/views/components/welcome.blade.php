      const orderData = @json($data);

      // List of months (ensure it matches the months from the database query)
      const months = ['Jan', 'Feb', 'Mar', 'Apr', 'May', 'Jun', 'Jul', 'Aug', 'Sep', 'Oct', 'Nov', 'Dec'];

      // Fill in the order data, with 0 for months without orders
      const data = months.map(month => orderData[month] ?? 0);

      // Create the chart
      const ctx = document.getElementById('myChart').getContext('2d');
      new Chart(ctx, {
        type: 'bar',
        data: {
          labels: months,
          datasets: [{
            label: 'Order Costs',
            data: data, // Use the data filled above
            backgroundColor: [
              'rgba(255, 99, 132, 0.2)',
              'rgba(255, 159, 64, 0.2)',
